public class Exercicio7 {
	// Para vários tributos, a base de cálculo é o salário mínimo. Fazer um
	// algoritmo que leia o valor do salário mínimo e o valor do salário de uma
	// pessoa.
	// Calcular e imprimir quantos salários mínimos ela ganha.

	public static void main(String[] args) {
		double min_sal = 1412.0;
		double curr_sal = 2824.0;

		System.out.println("Você ganha " + (curr_sal / min_sal) + " salários mínimos");
	}
}
